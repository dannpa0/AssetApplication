    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        Class table = jTable1.getModel().getClass();
        if (evt.getClickCount() == 1) {
            if (table.equals(JenisTableModel.class)) {
                JenisTableModel theModel = (JenisTableModel) jTable1.getModel();

                List<JenisAset> listJenis = theModel.getListJenisAset();
                jenisAset = listJenis.get(jTable1.getSelectedRow());

                txtJenisKode.setText(jenisAset.getKodeJenisAset());
                txtJenisNama.setText(jenisAset.getNamaJenisAset());

            }
            if (table.equals(KategoriTableModel.class)) {
                KategoriTableModel theModel = (KategoriTableModel) jTable1.getModel();

                List<KategoriAset> listJenis = theModel.getListKategoriAset();
                kategoriAset = listJenis.get(jTable1.getSelectedRow());

                txtKategoriKode.setText(kategoriAset.getKodeKategoriAset());
                txtKategoriNama.setText(kategoriAset.getNamaKategoriAset());

            }
            if (table.equals(KondisiTableModel.class)) {
                KondisiTableModel theModel = (KondisiTableModel) jTable1.getModel();

                List<KondisiAset> listKondisi = theModel.getListKondisiAset();
                kondisiAset = listKondisi.get(jTable1.getSelectedRow());

                txtKondisiKode.setText(kondisiAset.getKodeKondisiAset());
                txtKondisiNama.setText(kondisiAset.getNamaKondisiAset());

            }
            if (table.equals(LokasiTableModel.class)) {
                LokasiTableModel theModel = (LokasiTableModel) jTable1.getModel();

                List<LokasiAset> listLokasi = theModel.getListLokasiAset();
                lokasiAset = listLokasi.get(jTable1.getSelectedRow());

                txtLokasiKode.setText(lokasiAset.getKodeLokasiAset());
                txtLokasiNama.setText(lokasiAset.getNamaLokasiAset());

            }//            System.out.println(jenisAset.getNamaJenisAset());

            if (table.equals(AsetTableModel.class)) {
                AsetTableModel theModel = (AsetTableModel) jTable1.getModel();

                List<Aset> listAset = theModel.getListAset();
                Aset aset = listAset.get(jTable1.getSelectedRow());
                _aset = listAset.get(jTable1.getSelectedRow());
                jenisAset = _aset.getJenisAset();
                kategoriAset = _aset.getKategoriAset();
                kondisiAset = _aset.getKondisiAset();
//                lokasiAset = listAset.get(jTable1.getSelectedRow()).getLokasi();

                txtAsetKode.setText(_aset.getKodeAset());
                txtAsetNama.setText(_aset.getNamaAset());
                txtAsetJenis.setText(jenisAset.getNamaJenisAset());
                txtAsetKategori.setText(kategoriAset.getNamaKategoriAset());

                txtAsetPemakaian.setText(String.valueOf(_aset.getPemakaian()));
                txtAsetNilai.setText(String.valueOf(_aset.getNilaiAset()));
                txtAsetLokasi.setText(lokasiAset.getNamaLokasiAset());

//                txtAsetJumlah.setText(String.valueOf(alokasiAset.getJumlahAlokasiAset()));

                txtAsetKondisi.setText(kondisiAset.getNamaKondisiAset());

            }
            
            if (table.equals(UserTableModel.class)) {
                UserTableModel theModel = (UserTableModel) jTable1.getModel();

                List<User> listUser = theModel.getListUser();
                userModel = listUser.get(jTable1.getSelectedRow());

                txtUsername.setText(userModel.getUsername());
                txtPassword.setText(userModel.getPassword());
                txtRole.setText(userModel.getRole().getRoleName());

            }//            System.out.println(jenisAset.getNamaJenisAset());

        }

    }//GEN-LAST:event_jTable1MouseClicked

