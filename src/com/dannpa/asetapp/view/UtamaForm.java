    private void btnSaveRecordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveRecordActionPerformed
        // TODO add your handling code here:
        AsetController asetController = new AsetController(context);
        JenisController jenisController = new JenisController(context);
        KategoriController kategoriController = new KategoriController(context);
        KondisiController kondisiController = new KondisiController(context);
        AlokasiController alokasiController = new AlokasiController(context);
        LokasiController lokasiController = new LokasiController(context);
        UserController userController = new UserController(context);

        if (aset) {
            String kodeAset = txtAsetKode.getText();
            String namaAset = txtAsetNama.getText();
            int pemakaian = Integer.parseInt(txtAsetPemakaian.getText().length() > 0 ? txtAsetPemakaian.getText() : "0");
            int jumlah = Integer.parseInt(txtAsetJumlah.getText().length() > 0 ? txtAsetJumlah.getText() : "0");
            double nilai = Double.parseDouble(txtAsetNilai.getText().length() > 0 ? txtAsetNilai.getText() : "0");
            long tglTerima = dateChooserTglTerima.getCalendar().getTimeInMillis();

            Aset aset = new Aset();
            aset.setKodeAset(kodeAset);
            aset.setNamaAset(namaAset);
            aset.setPemakaian(pemakaian);
            aset.setJumlahAset(jumlah);
            aset.setTglTerimaAset(tglTerima);
            aset.setNilaiAset(nilai);
            aset.setJenisAset(jenisAset);
            aset.setKondisiAset(kondisiAset);
            aset.setKategoriAset(kategoriAset);
            if (penyusutan) {
                int umurEkonomis = Integer.parseInt(txtAsetUmurEkonomis.getText());
                double nilaiPenyusutan = Double.parseDouble(txtAsetPenyusutan.getText());
                PenyusutanAset penyusutan = new PenyusutanAset();

                penyusutan.setUmurEkonomis(umurEkonomis);
                penyusutan.setJumlahPenyusutan(nilaiPenyusutan);

                aset.setPenyusutanAset(penyusutan);
                asetController.insertAset(aset);
            } else {
                asetController.insertAset(aset);

                AlokasiAset alokasiAset = new AlokasiAset();
                alokasiAset.setAset(aset);
                alokasiAset.setLokasi(lokasiAset);
//            alokasiAset.setLokasi(lokasiAset);

                alokasiController.insert(alokasiAset);

            }

            clearAllOfAset();

        }

        if (jenis) {
            String kodeJenis = txtJenisKode.getText();
            String namaJenis = txtJenisNama.getText();

            JenisAset jenisAset = new JenisAset();
            jenisAset.setKodeJenisAset(kodeJenis);
            jenisAset.setNamaJenisAset(namaJenis);

            jenisController.insertAset(jenisAset);

            clearAllOfJenis();
        }

        if (kondisi) {
            String kodeKondisi = txtKondisiKode.getText();
            String namaKondisi = txtKondisiNama.getText();

            KondisiAset kondisiAset = new KondisiAset();
            kondisiAset.setKodeKondisiAset(kodeKondisi);
            kondisiAset.setNamaKondisiAset(namaKondisi);

            kondisiController.insert(kondisiAset);

            clearAllOfKondisi();

        }

        if (kategori) {
            String kodeKategori = txtKategoriKode.getText();
            String namaKategori = txtKategoriNama.getText();

            KategoriAset kategoriAset = new KategoriAset();
            kategoriAset.setKodeKategoriAset(kodeKategori);
            kategoriAset.setNamaKategoriAset(namaKategori);

            kategoriController.insert(kategoriAset);
            clearAllOfKategori();
        }

        if (lokasi) {
            String kodeLokasi = txtLokasiKode.getText();
            String namaLokasi = txtLokasiNama.getText();

            LokasiAset lokasiAset = new LokasiAset();
            lokasiAset.setKodeLokasiAset(kodeLokasi);
            lokasiAset.setNamaLokasiAset(namaLokasi);

            lokasiController.insert(lokasiAset);

            clearAllOfLokasi();
        }
        
        if(pengguna){
            String username = txtUsername.getText();
            String password = txtPassword.getText();
            
            User user = new User();
            user.setUsername(username);
            user.setPassword(password);
            user.setRole(role);
            
            userController.insert(user);
            
            clearAllOfUser();
            
            
        }

        normalState();


    }//GEN-LAST:event_btnSaveRecordActionPerformed

