    @Override
    public void update(Observable o, Object arg) {

        Class asd = arg.getClass();

        if (JenisAset.class.equals(asd)) {
            System.out.println("jenis");
            jenisAset = (JenisAset) arg;
            txtAsetJenis.setText(((JenisAset) arg).getNamaJenisAset());

        }

        if (KategoriAset.class
                .equals(asd)) {
            System.out.println("kategori");
            kategoriAset = (KategoriAset) arg;
            txtAsetKategori.setText(((KategoriAset) arg).getNamaKategoriAset());
            if (kategoriAset.getNamaKategoriAset().equals("ASET TETAP")) {
                txtAsetUmurEkonomis.setEditable(true);
                txtAsetPenyusutan.setEditable(true);
                penyusutan = true;
            } else {
                txtAsetUmurEkonomis.setEditable(false);
                txtAsetPenyusutan.setEditable(false);
                penyusutan = false;
            }

        }

        if (KondisiAset.class
                .equals(asd)) {
            System.out.println("kondisi");
            kondisiAset = (KondisiAset) arg;
            txtAsetKondisi.setText(((KondisiAset) arg).getNamaKondisiAset());

        }

        if (LokasiAset.class
                .equals(asd)) {
            System.out.println("lokasi");
            lokasiAset = (LokasiAset) arg;
            txtAsetLokasi.setText(((LokasiAset) arg).getNamaLokasiAset());
        }
        
        if(Role.class.equals(asd)){
            System.out.println("role");
            role = (Role) arg;
            txtRole.setText((role.getRoleName()));
        }

    }

